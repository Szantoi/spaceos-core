# Generate state hook with Zustand-like pattern
generate_state_hook() {
    local hook_file="$HOOKS_DIR/use${HOOK_NAME}.ts"

    cat > "$hook_file" << EOF
/**
 * use${HOOK_NAME} - State management hook
 * Generated by SpaceOS Code Generator (ADR-050)
 */

import { useState, useCallback, useMemo } from 'react';

export interface ${HOOK_NAME}State {
  // TODO: Add your state properties
  value: string;
  isOpen: boolean;
}

export interface Use${HOOK_NAME}Actions {
  setValue: (value: string) => void;
  setIsOpen: (isOpen: boolean) => void;
  toggle: () => void;
  reset: () => void;
}

export interface Use${HOOK_NAME}Return extends ${HOOK_NAME}State, Use${HOOK_NAME}Actions {}

const initial${HOOK_NAME}State: ${HOOK_NAME}State = {
  value: '',
  isOpen: false,
};

export const use${HOOK_NAME} = (initialState?: Partial<${HOOK_NAME}State>): Use${HOOK_NAME}Return => {
  const [state, setState] = useState<${HOOK_NAME}State>({
    ...initial${HOOK_NAME}State,
    ...initialState,
  });

  const setValue = useCallback((value: string) => {
    setState(prev => ({ ...prev, value }));
  }, []);

  const setIsOpen = useCallback((isOpen: boolean) => {
    setState(prev => ({ ...prev, isOpen }));
  }, []);

  const toggle = useCallback(() => {
    setState(prev => ({ ...prev, isOpen: !prev.isOpen }));
  }, []);

  const reset = useCallback(() => {
    setState(initial${HOOK_NAME}State);
  }, []);

  return useMemo(() => ({
    ...state,
    setValue,
    setIsOpen,
    toggle,
    reset,
  }), [state, setValue, setIsOpen, toggle, reset]);
};

export default use${HOOK_NAME};
EOF

    log_success "Created: hooks/use${HOOK_NAME}.ts (state)"
}

# Generate effect hook
generate_effect_hook() {
    local hook_file="$HOOKS_DIR/use${HOOK_NAME}.ts"

    cat > "$hook_file" << EOF
/**
 * use${HOOK_NAME} - Side effect hook
 * Generated by SpaceOS Code Generator (ADR-050)
 */

import { useEffect, useRef, useCallback } from 'react';

export interface Use${HOOK_NAME}Options {
  // TODO: Add your options
  enabled?: boolean;
  debounceMs?: number;
}

export interface Use${HOOK_NAME}Return {
  // TODO: Define return type based on your effect
}

export const use${HOOK_NAME} = (
  callback: () => void | (() => void),
  options: Use${HOOK_NAME}Options = {}
): Use${HOOK_NAME}Return => {
  const { enabled = true, debounceMs = 0 } = options;
  const callbackRef = useRef(callback);
  const timeoutRef = useRef<NodeJS.Timeout>();

  // Keep callback ref up to date
  useEffect(() => {
    callbackRef.current = callback;
  }, [callback]);

  useEffect(() => {
    if (!enabled) return;

    const execute = () => {
      const cleanup = callbackRef.current();
      return cleanup;
    };

    let cleanup: void | (() => void);

    if (debounceMs > 0) {
      timeoutRef.current = setTimeout(() => {
        cleanup = execute();
      }, debounceMs);
    } else {
      cleanup = execute();
    }

    return () => {
      if (timeoutRef.current) {
        clearTimeout(timeoutRef.current);
      }
      if (typeof cleanup === 'function') {
        cleanup();
      }
    };
  }, [enabled, debounceMs]);

  // Return empty object - customize based on your needs
  return {} as Use${HOOK_NAME}Return;
};

export default use${HOOK_NAME};
EOF

    log_success "Created: hooks/use${HOOK_NAME}.ts (effect)"
}

