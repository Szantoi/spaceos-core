main() {
    parse_args "$@"

    # Validate hook name (camelCase, no 'use' prefix)
    if [[ "$HOOK_NAME" =~ ^use ]]; then
        log_warn "Hook name should not start with 'use' - it will be added automatically"
        HOOK_NAME="${HOOK_NAME#use}"
    fi

    # Capitalize first letter
    HOOK_NAME="$(echo "${HOOK_NAME:0:1}" | tr '[:lower:]' '[:upper:]')${HOOK_NAME:1}"

    log_info "Generating ${HOOK_TYPE} hook: use${HOOK_NAME}"
    log_info "Output: $HOOKS_DIR/use${HOOK_NAME}.ts"

    # Ensure hooks directory exists
    mkdir -p "$HOOKS_DIR"

    # Generate based on type
    case "$HOOK_TYPE" in
        query)
            generate_query_hook
            ;;
        mutation)
            generate_mutation_hook
            ;;
        state)
            generate_state_hook
            ;;
        effect)
            generate_effect_hook
            ;;
    esac

    # Generate test if requested
    if [ "$WITH_TEST" = true ]; then
        generate_test
    fi

    echo ""
    log_success "Hook use${HOOK_NAME} generated successfully!"
    echo ""
    log_info "Next steps:"
    echo "  1. Open $HOOKS_DIR/use${HOOK_NAME}.ts"
    echo "  2. Update the interface types"
    echo "  3. Import: import { use${HOOK_NAME} } from '@/hooks/use${HOOK_NAME}'"
}

main "$@"
