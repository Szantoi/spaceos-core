#!/bin/bash
#
# generate-hook.sh — SpaceOS React Hook Generator
# Part of SpaceOS Code Generator Toolchain (ADR-050 Phase 4.5)
#
# Usage:
#   ./generate-hook.sh <name> [options]
#
# Options:
#   --type <query|mutation|state|effect>   Hook type (default: query)
#   --with-test                            Include test file
#   --with-cache                           Add TanStack Query caching
#   --endpoint <path>                      API endpoint for query/mutation hooks
#

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
SPACEOS_ROOT="/opt/spaceos"
HOOKS_DIR="$SPACEOS_ROOT/datahaven-web/client/src/hooks"

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
NC='\033[0m'

log_info() { echo -e "${BLUE}[INFO]${NC} $1"; }
log_success() { echo -e "${GREEN}[SUCCESS]${NC} $1"; }
log_warn() { echo -e "${YELLOW}[WARN]${NC} $1"; }
log_error() { echo -e "${RED}[ERROR]${NC} $1"; }

# Default values
HOOK_NAME=""
HOOK_TYPE="query"
WITH_TEST=false
WITH_CACHE=false
ENDPOINT=""

show_usage() {
    echo -e "${CYAN}SpaceOS Hook Generator${NC}"
    echo ""
    echo "Usage: $0 <name> [options]"
    echo ""
    echo "Arguments:"
    echo "  name                            Hook name (camelCase, without 'use' prefix)"
    echo ""
    echo "Options:"
    echo "  --type <query|mutation|state|effect>   Hook type (default: query)"
    echo "  --with-test                            Include test file"
    echo "  --with-cache                           Add TanStack Query caching (query/mutation only)"
    echo "  --endpoint <path>                      API endpoint path (e.g., /api/orders)"
    echo ""
    echo "Examples:"
    echo "  $0 Orders --type query --with-cache --endpoint /api/orders"
    echo "  $0 Auth --type state --with-test"
    echo "  $0 CreateQuote --type mutation --endpoint /api/quotes"
    echo "  $0 WindowResize --type effect"
}

parse_args() {
    if [ $# -lt 1 ]; then
        show_usage
        exit 1
    fi

    # Handle --help/-h as first argument
    if [[ "$1" == "--help" || "$1" == "-h" ]]; then
        show_usage
        exit 0
    fi

    HOOK_NAME="$1"
    shift

    while [ $# -gt 0 ]; do
        case "$1" in
            --type)
                HOOK_TYPE="$2"
                if [[ ! "$HOOK_TYPE" =~ ^(query|mutation|state|effect)$ ]]; then
                    log_error "Invalid hook type: $HOOK_TYPE. Must be query, mutation, state, or effect."
                    exit 1
                fi
                shift 2
                ;;
            --with-test)
                WITH_TEST=true
                shift
                ;;
            --with-cache)
                WITH_CACHE=true
                shift
                ;;
            --endpoint)
                ENDPOINT="$2"
                shift 2
                ;;
            -h|--help)
                show_usage
                exit 0
                ;;
            *)
                log_error "Unknown option: $1"
                show_usage
                exit 1
                ;;
        esac
    done
}

# Generate query hook with TanStack Query
generate_query_hook() {
    local hook_file="$HOOKS_DIR/use${HOOK_NAME}.ts"
    local api_endpoint="${ENDPOINT:-/api/${HOOK_NAME,,}s}"

    if [ "$WITH_CACHE" = true ]; then
        cat > "$hook_file" << EOF
/**
 * use${HOOK_NAME} - Query hook with TanStack Query caching
 * Generated by SpaceOS Code Generator (ADR-050)
 */

import { useQuery, UseQueryOptions } from '@tanstack/react-query';

export interface ${HOOK_NAME}Data {
  id: string;
  // TODO: Add your data properties
}

export interface Use${HOOK_NAME}Options {
  enabled?: boolean;
  refetchInterval?: number;
}

const fetch${HOOK_NAME} = async (): Promise<${HOOK_NAME}Data[]> => {
  const response = await fetch('${api_endpoint}', {
    credentials: 'include',
  });

  if (!response.ok) {
    throw new Error(\`Failed to fetch ${HOOK_NAME}: \${response.status}\`);
  }

  return response.json();
};

export const use${HOOK_NAME} = (options: Use${HOOK_NAME}Options = {}) => {
  const { enabled = true, refetchInterval } = options;

  return useQuery({
    queryKey: ['${HOOK_NAME,,}'],
    queryFn: fetch${HOOK_NAME},
    enabled,
    refetchInterval,
    staleTime: 5 * 60 * 1000, // 5 minutes
    gcTime: 30 * 60 * 1000, // 30 minutes (formerly cacheTime)
  });
};

export default use${HOOK_NAME};
EOF
    else
        cat > "$hook_file" << EOF
/**
 * use${HOOK_NAME} - Basic query hook
 * Generated by SpaceOS Code Generator (ADR-050)
 */

import { useState, useEffect } from 'react';

export interface ${HOOK_NAME}Data {
  id: string;
  // TODO: Add your data properties
}

